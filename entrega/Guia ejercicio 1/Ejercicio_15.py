# Inicializar una variable llamada sociedad con el valor inicial de ‘aiPython P1’
# a. Imprimir la variable utilizando print()
# b. Imprimir la longitud de la variable sociedad usando len() y print()
# c. Cambiar todos los caracteres a mayúsculas usando el método upper()
# d. Cambiar todos los caracteres a minúsculas usando el método lower()

# Inicializar la variable sociedad
sociedad = 'aiPython P1'

# Imprimir la variable utilizando print()
print("a. Valor de la variable sociedad:", sociedad)

# Imprimir la longitud de la variable sociedad usando len() y print()
longitud = len(sociedad)
print("b. Longitud de la variable sociedad:", longitud)

# Cambiar todos los caracteres a mayúsculas usando el método upper()
sociedad_mayusculas = sociedad.upper()
print("c. En mayúsculas:", sociedad_mayusculas)

# Cambiar todos los caracteres a minúsculas usando el método lower()
sociedad_minusculas = sociedad.lower()
print("d. En minúsculas:", sociedad_minusculas)
