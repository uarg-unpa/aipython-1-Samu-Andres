# Usar los métodos capitalize(), title(), swapcase() para formatear el valor del string “sometimes it is the people no one imagines anything of who do the things that no one can imagine.”

texto = "sometimes it is the people no one imagines anything of who do the things that no one can imagine."

# Aplicar el método capitalize() para capitalizar la primera letra de la cadena
capitalizado = texto.capitalize()

# Aplicar el método title() para capitalizar la primera letra de cada palabra en la cadena
titulizado = texto.title()

# Aplicar el método swapcase() para cambiar entre mayúsculas y minúsculas
intercambiado = texto.swapcase()

# Imprimir los resultados
print("capitalize():", capitalizado)
print("title():", titulizado)
print("swapcase():", intercambiado)