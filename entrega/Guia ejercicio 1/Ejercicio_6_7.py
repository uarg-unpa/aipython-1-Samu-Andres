#EJERCICIO 6
# Solicito dos números enteros al usuario
num1 = int(input("Introduce el primer número entero: "))
num2 = int(input("Introduce el segundo número entero: "))

# Se realiza las operaciones
suma = num1 + num2
resta = num1 - num2
producto = num1 * num2
potencia = num1 ** num2
resto = num1 % num2

# Muestro los resultados
print(f"La suma de {num1} y {num2} es: {suma}")
print(f"La resta de {num1} y {num2} es: {resta}")
print(f"El producto de {num1} y {num2} es: {producto}")
print(f"{num1} elevado a la {num2} es: {potencia}")
print(f"El resto de {num1} dividido por {num2} es: {resto}")

